let person = {
    name:       'Bob',
    occupation: 'web developer',
    hobbies:    'painting',
};

// Truy cập các key của object
// Cách 1: Dot Notation
console.log(person.name);
console.log(person.occupation);
console.log(person.hobbies);

// Cách 2: Bracket Notation
console.log(person["name"]);
console.log(person["occupation"]);
console.log(person["hobbies"]);
